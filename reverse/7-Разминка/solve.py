def ror3(x):
    return ((x >> 3) | ((x & 0x07) << 5)) & 0xFF

encrypted_bytes = [
    0x22, 0xAA, 0x1A, 0x5A, 0x2A, 0x92, 0xD2, 0xDB, 0x99, 0x0A,
    0xA9, 0xCB, 0xFA, 0x81, 0x83, 0x99, 0x93, 0x0A, 0xB9, 0x4B,
    0x81, 0x72, 0xFA, 0xA9, 0x81, 0x63, 0xB3, 0x99, 0x23, 0xEB
]

original = ''.join(chr(ror3(b)) for b in encrypted_bytes)
print(original)